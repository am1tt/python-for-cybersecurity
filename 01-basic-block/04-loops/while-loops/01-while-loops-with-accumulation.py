

## here we will see how to use while loops # 


number = 5

while number > 0: 
    print(number)  
    number -= 1 


## here i also used accumulation to reduce the value of number by 1 each time the loop runs ##

## resulting in printing : 5 4 3 2 1 ## 

#without accumulation it would be an infinite loop as 5 would always be greater than 0 ## 