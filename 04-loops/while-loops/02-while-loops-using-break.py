

# here we will use break statement in while loop # 


number = 5 

while number != 0:
    print(number)
    number -= 1 
    if number == 2:
        print("breaking the loop")
        break



## another example of break statement in while loop ## 



num = 8

while num >= 0:
    num -= 1
    if num == 2:
        print("breaking as i dont want to print 5")
        break
    print(num) 


        
